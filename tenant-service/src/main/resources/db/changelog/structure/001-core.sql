    NAME VARCHAR(255),
    EMAIL VARCHAR(255),
    COMPANY VARCHAR(255),
    COUNTRY_NAME VARCHAR(100),
    COUNTRY_CODE VARCHAR(10),
    CITY VARCHAR(100),
    STATE VARCHAR(100),
    WEBSITE VARCHAR(500),
    BIO TEXT,
