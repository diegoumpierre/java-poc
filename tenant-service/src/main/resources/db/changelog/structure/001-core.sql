-- ===========================================================================
-- TNT_CORE_TENANTS
-- ===========================================================================

--changeset tenant:schema-core-tenants
CREATE TABLE IF NOT EXISTS TNT_CORE_TENANTS (
    ID CHAR(36) NOT NULL,
    NAME VARCHAR(255) NOT NULL,
    SLUG VARCHAR(255) NOT NULL,
    TENANT_TYPE VARCHAR(20) NOT NULL DEFAULT 'CLIENT',
    STATUS VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    SUBSCRIPTION_STATUS VARCHAR(20) DEFAULT 'TRIAL',
    PARENT_TENANT_ID CHAR(36),
    IS_OWNER TINYINT(1) DEFAULT 0,
    BILLING_MODEL VARCHAR(20) DEFAULT 'DIRECT',
    COMMISSION_RATE DECIMAL(5,2) DEFAULT 0.00,
    WHOLESALE_DISCOUNT DECIMAL(5,2) DEFAULT 0.00,
    FINANCE_WALLET_ID CHAR(36) NULL,
    KANBAN_BOARD_ID CHAR(36) NULL,
    PROVISIONING_STATUS VARCHAR(50) DEFAULT 'COMPLETE',
    TRIAL_ENDS_AT TIMESTAMP(6),
    SUSPENDED_AT TIMESTAMP(6),
    SUSPENSION_REASON VARCHAR(500),
    CREATED_BY CHAR(36),
    DELETED_AT TIMESTAMP(6),
    DELETED_BY CHAR(36),
    CREATED_AT TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP(6),
    UPDATED_AT TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP(6),
    PRIMARY KEY (ID),
    UNIQUE KEY UK_TNT_CORE_TENANTS_SLUG (SLUG),
    INDEX IDX_TNT_CORE_TENANTS_PARENT (PARENT_TENANT_ID),
    INDEX IDX_TNT_CORE_TENANTS_TYPE (TENANT_TYPE),
    INDEX IDX_TNT_CORE_TENANTS_STATUS (STATUS),
    CONSTRAINT FK_TNT_CORE_TENANTS_PARENT FOREIGN KEY (PARENT_TENANT_ID) REFERENCES TNT_CORE_TENANTS(ID) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

