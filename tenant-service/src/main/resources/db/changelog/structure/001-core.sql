    PRICE DECIMAL(15,2),
    RATING INT,
    IMAGE VARCHAR(500),
    REPRESENTATIVE_NAME VARCHAR(255),
    REPRESENTATIVE_IMAGE VARCHAR(500),
    TENANT_ID CHAR(36),
    EXTERNAL_ID VARCHAR(255),
    KIND VARCHAR(50),
    METADATA TEXT,
    PHONE VARCHAR(50),
    CREATED_AT TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP(6),
    UPDATED_AT TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP(6),
    PRIMARY KEY (ID),
